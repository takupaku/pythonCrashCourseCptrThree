location=["italy","paris","japan","korea","china"]
print(location)
print(sorted(location))  #sorted()--->temporarily,sort()--->permanently
print(location)

location.reverse()
print(location)
location.reverse()
print(location)     #reverse again

location.sort()
print(location)

location.sort(reverse=True)   #said will be reversed, but haven't
print(location)